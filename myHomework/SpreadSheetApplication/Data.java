public class Data {
  private String item;
  private int value;

  public Data(String item, int value) {
    this.item = item;
    this.value = value;
  }

  public String getItem() {
    return item;
  }

  public void setItem(String item) {
    this.item = item;
  }

  public int getValue() {
    return value;
  }

  public void setValue(int value) {
    this.value = value;
  }
}
